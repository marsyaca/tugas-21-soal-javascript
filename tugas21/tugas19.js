// Input tiga angka 
let x = 30;
let y = 50;
let z = 45;

// Cari nilai terbesar
let terbesar = Math.max(x, y, z);

// Cari nilai terkecil
let terkecil = Math.min(x, y, z);

// Tampilkan hasil
console.log("Nilai terbesar adalah: " + terbesar);
console.log("Nilai terkecil adalah: " + terkecil);